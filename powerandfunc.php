<div class="nu">
	<ol>
		<li>Inquire, on its own initiative or on a petition presented to it by a victim or any person
on his behalf, into complaint of-
		</li>
		 
		<ol type="i">
			<li>Violation of human rights or abetment or
			</li>
			<li>Negligence in the prevention of such violation, by a public servant;
			</li>
		</ol>
		 
		<li>Intervene in any proceeding involving any allegation of violation of human rights
pending before a court with the approval of such court;
		</li>
		 
		<li>Visit, under intimation to the State Government, any jail or any other institution under
the control of the State Government, where persons are detained or lodged for purposes of
treatment, reformation or protection to study the living condition of the inmates and make
recommendations thereon;
		</li>
		 
		<li>Review the safeguards by or under the Constitution or any law for the time being in
force for the protection of human rights and recommend measures for their effective
implementation;
		</li>
		 
		<li>Study treaties and other international instruments on human rights and make
recommendations for their effective implementation;
		</li>
		 
		<li>Undertake and promote research in the field of human rights;
		</li>
		 
		<li>Spread human rights literacy among various sections of society and promote
awareness of the safeguards available for the protection of these rights through
publications, the media, seminars and other available means;
		</li>
		 
		<li>Encourage the efforts of non-Governmental organizations and institutions working in
the field of human rights;
		</li>
		 
		<li>Such other functions as it may consider necessary for the promotion of human rights.
		</li>
		 

</ol>
