		Human Rights
		<hr>
	</div>
	<div class="nu">
		In terms of Section 2 of the Protection of Human Rights Act, 1993 - "Human Rights" means the rights to relating to life, liberty, equality and dignity of the individual guaranteed by the constitution or embodied  in the Internationnal covenants and enforceable by courts in India. "International Covenants" means the International Covenant on Civil and Political Rights and the International Covenant on Economic, Social and Cultural Rights adopted by the General Assembly of the United Nations on the 16th December, 1966.
